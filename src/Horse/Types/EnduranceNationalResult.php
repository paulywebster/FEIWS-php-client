<?php

namespace FEIWebServicesClient\Horse\Types;

class EnduranceNationalResult
{

    /**
     * @var float
     */
    private $AverageSpeed = null;

    /**
     * @var float
     */
    private $Distance = null;

    /**
     * @var int
     */
    private $RankPosition = null;

    /**
     * @var string
     */
    private $VenueName = null;

    /**
     * @var string
     */
    private $CountryCode = null;

    /**
     * @var \DateTime
     */
    private $CompetitionDate = null;

    /**
     * @return float
     */
    public function getAverageSpeed() : float
    {
        return $this->AverageSpeed;
    }

    /**
     * @return float
     */
    public function getDistance() : float
    {
        return $this->Distance;
    }

    /**
     * @return int
     */
    public function getRankPosition() : int
    {
        return $this->RankPosition;
    }

    /**
     * @return string
     */
    public function getVenueName() : string
    {
        return $this->VenueName;
    }

    /**
     * @return string
     */
    public function getCountryCode() : string
    {
        return $this->CountryCode;
    }

    /**
     * @return \DateTime
     */
    public function getCompetitionDate() : \DateTime
    {
        return $this->CompetitionDate;
    }


}

